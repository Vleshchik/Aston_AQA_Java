public class printColor {
    public static void main(String[] args) {
        int num = 100;

        if (num > 100) {
            System.out.println("Зеленый");
        }else if (num <= 0) {
            System.out.println("Красный");
        } else {
            System.out.println("Желтый");
        }
    }
}
